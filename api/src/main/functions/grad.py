    def get_department_table(self, department_name):
        """학과명을 입력받아 해당 학과의 졸업이수학점 마크다운 테이블을 반환"""
        # 일반 학과 검색
        if department_name in self.department_tables:
            return self.department_tables[department_name]

        # 연계전공 검색
        if department_name in self.interdisciplinary_majors:
            return f"## {department_name} (연계전공)\n\n{self.interdisciplinary_majors[department_name]}"

        # 융합전공 검색
        if department_name in self.convergence_majors:
            return f"## {department_name} (융합전공)\n\n{self.convergence_majors[department_name]}"

        # DSC 공유대학 검색
        if department_name in self.dsc_majors:
            return f"## {department_name} (DSC 공유대학)\n\n{self.dsc_majors[department_name]}"

        # 찾을 수 없는 경우
        all_departments = list(self.department_tables.keys()) + list(self.interdisciplinary_majors.keys()) + list(self.convergence_majors.keys()) + list(self.dsc_majors.keys())
        return f"'{department_name}'을(를) 찾을 수 없습니다.\n\n**전체 학과/전공 목록:**\n" + "\n".join(f"- {dept}" for dept in sorted(all_departments))

    def get_all_departments_by_college(self):
        """대학별 학과 목록을 마크다운 테이블로 반환"""
        result = "# 충남대학교 전체 학과 목록\n\n"

        # 일반 대학
        for college, departments in self.colleges.items():
            result += f"## {college}\n\n"
            result += "| 학과명 | 비고 |\n"
            result += "|--------|------|\n"
            for dept in departments:
                result += f"| {dept} | - |\n"
            result += "\n"

        # 연계전공
        result += "## 연계전공\n\n"
        result += "| 전공명 | 학위 | 비고 |\n"
        result += "|--------|------|------|\n"
        for major, info in self.interdisciplinary_majors.items():
            degree = info.split(" - ")[0]
            credit_info = info.split(" - ")[1] if " - " in info else ""
            result += f"| {major} | {degree} | {credit_info} |\n"
        result += "\n"

        # 융합전공
        result += "## 융합전공\n\n"
        result += "| 전공명 | 학위 | 비고 |\n"
        result += "|--------|------|------|\n"
        for major, info in self.convergence_majors.items():
            degree = info.split(" - ")[0]
            credit_info = info.split(" - ")[1] if " - " in info else ""
            result += f"| {major} | {degree} | {credit_info} |\n"
        result += "\n"

        # DSC 공유대학
        result += "## DSC 공유대학\n\n"
        result += "| 전공명 | 소속학부 | 비고 |\n"
        result += "|--------|----------|------|\n"
        for major, info in self.dsc_majors.items():
            parts = info.split(" - ")
            department = parts[0]
            credit_info = parts[1] if len(parts) > 1 else ""
            result += f"| {major} | {department} | {credit_info} |\n"

        return result

    def get_all_departments(self):
        """모든 학과/전공 목록 반환"""
        all_depts = []
        all_depts.extend(list(self.department_tables.keys()))
        all_depts.extend(list(self.interdisciplinary_majors.keys()))
        all_depts.extend(list(self.convergence_majors.keys()))
        all_depts.extend(list(self.dsc_majors.keys()))
        return sorted(all_depts)

    def search_department(self, keyword):
        """키워드로 학과/전공 검색"""
        all_depts = self.get_all_departments()
        matches = [dept for dept in all_depts if keyword in dept]
        return matches


